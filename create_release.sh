#!/bin/bash

# 创建发布目录
mkdir -p "PDF页面顺序反转工具_发布版"
mkdir -p "PDF页面顺序反转工具_发布版/程序"

# 复制文件
cp output/PDF页面顺序反转工具.exe "PDF页面顺序反转工具_发布版/程序/"
cp pdf_reverser.png "PDF页面顺序反转工具_发布版/程序/"

# 创建说明文档
cat > "PDF页面顺序反转工具_发布版/使用说明.txt" << EOL
PDF页面顺序反转工具 v1.0

功能说明：
将PDF文件的页面顺序反转，从最后一页到第一页重新排序。

使用方法：
1. 运行 程序/PDF页面顺序反转工具.exe
2. 点击"浏览"选择要处理的PDF文件
3. 点击"开始转换"
4. 等待处理完成

注意事项：
- 原文件不会被修改
- 新文件会保存在原文件相同目录下
- 新文件名格式：原文件名_reversed.pdf
EOL

# 创建压缩包
zip -r "PDF页面顺序反转工具_v1.0.zip" "PDF页面顺序反转工具_发布版"

echo "发布包创建完成：PDF页面顺序反转工具_v1.0.zip" 